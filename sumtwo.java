import java.util.*;

public class sumtwo {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the first number");
        int n = sc.nextInt();
        System.out.println("Enter the second number");
        int m = sc.nextInt();
        int sum = n + m;
        System.out.println("The sum of " + n + " and " + m + " is " + sum);

    }

}
